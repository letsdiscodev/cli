#!/usr/bin/env node
// Refresh src/llm/narrative.ts from https://disco.cloud/llms.txt.
// Run before tagging a release — see RELEASE.md.

import {writeFileSync} from 'node:fs'
import {dirname, join} from 'node:path'
import {fileURLToPath} from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const projectRoot = join(__dirname, '..')
const outPath = join(projectRoot, 'src', 'llm', 'narrative.ts')

const url = 'https://disco.cloud/llms.txt'
console.log(`Fetching ${url}...`)

const res = await fetch(url)
if (!res.ok) {
  console.error(`Failed to fetch ${url}: ${res.status} ${res.statusText}`)
  process.exit(1)
}

const text = await res.text()
const escaped = text.replaceAll('\\', '\\\\').replaceAll('`', '\\`').replaceAll('${', '\\${')

const tsContent = `// Auto-generated by scripts/sync-llms-narrative.js — do not edit by hand.
// Source: ${url}
// Last synced: ${new Date().toISOString()}

export const NARRATIVE = \`${escaped}\`
`

writeFileSync(outPath, tsContent)
console.log(`Wrote ${outPath} (${text.length} chars)`)
